    /// 特殊物品 — 海角阁
    /// 翼家的仙蛊屋，曾被梦境惊吓后退。
    /// </summary>
    public class 海角阁 : ModItem
    {
        public override void SetDefaults()
        {
            Item.width = 24;
            Item.height = 24;
            Item.rare = ItemRarityID.LightPurple;
            Item.maxStack = 1;
            Item.value = 100000;
        }
    }
}
